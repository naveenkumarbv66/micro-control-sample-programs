  LEA DX,TEMP
  MOV AH,NUM
  INT 21H
ENDM

CONVERTION MACRO 
   LOCAL NEXT1
      CMP DL,0AH
      JL NEXT1
      ADD DL,07H
NEXT1: ADD DL,30H
      MOV AH,02H
      INT 21H
ENDM

PUT MACRO P1
    MOV DL,P1
    AND DL,0F0H
    MOV CL,04H
    ROR DL,CL
    CONVERTION
    MOV DL,P1
    AND DL,0FH
    CONVERTION
ENDM

